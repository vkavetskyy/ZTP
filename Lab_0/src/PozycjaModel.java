public class PozycjaModel {
    private String nazwaPozycji;
    private int iloscPozycji;
    private float cenaPozycji;

    public String getNazwaPozycji() {
        return nazwaPozycji;
    }

    public void setNazwaPozycji(String nazwaPozycji) {
        this.nazwaPozycji = nazwaPozycji;
    }

    public int getIloscPozycji() {
        return iloscPozycji;
    }

    public void setIloscPozycji(int iloscPozycji) {
        this.iloscPozycji = iloscPozycji;
    }

    public float getCenaPozycji() {
        return cenaPozycji;
    }

    public void setCenaPozycji(float cenaPozycji) {
        this.cenaPozycji = cenaPozycji;
    }

}
